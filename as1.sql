CREATE TABLE COURSE
(
	NAME		CHAR(24),
	COURSE		CHAR(10),
	CREDIT		CHAR(6),
	COLLEGE 	CHAR(20),
	HRS			NUMBER(1)
	CONSTRAINT UorG CHECK (CREDIT = 'U' or CREDIT = 'G'),

)
;

CREATE TABLE STUDENT 
(
	SID			VARCHAR2(7),
	NAME		CHAR(14),
	ADDRESS		CHAR(22),
	BIRTHDATE	DATE,
	GRADELEVEL 	CHAR(2)
)
;

CREATE TABLE SECTION
(
	SECTION		VARCHAR2(8),
	TIME		CHAR(5),
	MAXST		NUMBER(2),
	ROOM 		VARCHAR2(14)
	CONSTRAINT MaxStudents CHECK (MAXST <= 35)
)
;

CREATE TABLE TAKES
(
	GRADE		CHAR(5)
)
;
